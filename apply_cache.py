applied = 0
for row in rows:
    hid = row.get("Hospital ID (UUID)", "")
    if not hid:
        continue
    cached = done.get(hid)
    if not cached or not isinstance(cached, dict):
        continue
    addr = cached.get("Hospital Address", "")
    if addr and not row.get("Address"):
        row["Address"] = addr
    pin = cached.get("Hospital Pincode", "")
    if pin and not row.get("Pincode"):
        row["Pincode"] = pin
    state = cached.get("State", "") or row.get("State", "")
    district = cached.get("District", "") or row.get("Town", "")
    if state and not row.get("State"):
        row["State"] = state
    if district and not row.get("Town"):
        row["Town"] = district
    if addr and not row.get("Subdistrict"):
        first = addr.split(",")[0].strip()
        if first and len(first) < 60:
            row["Subdistrict"] = first
    if addr and not row.get("Village"):
        parts = [p.strip() for p in addr.split(",")]
        if len(parts) >= 1:
            row["Village"] = parts[0]
    lat = cached.get("Latitude", "")
    lon = cached.get("Longitude", "")
    if lat and not row.get("Latitude"):
        row["Latitude"] = lat
    if lon and not row.get("Longitude"):
        row["Longitude"] = lon
    if (not row.get("Latitude") or not row.get("Longitude")) and district:
        key = f"{state}|{district}".upper()
        if key in district_latlon:
            la, lo, _ = district_latlon[key]
            if not row.get("Latitude"):
                row["Latitude"] = la
            if not row.get("Longitude"):
                row["Longitude"] = lo
    if not row.get("Location") and district:
        row["Location"] = f"{district}, {state}"
    no_name = cached.get("PMJAY- Nodal Officer Name", "") or cached.get("Nodal Officer Name", "")
    no_num = cached.get("PMJAY-Nodal Officer Number", "") or cached.get("Nodal Officer Number", "")
    no_email = cached.get("Contact Person Email ID", "") or cached.get("Organisation Head Email ID", "")
